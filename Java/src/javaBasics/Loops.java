
package javaBasics;

public class Loops {

	public static void main(String[] args) {
		//while loop
		int i=1;
		while(i<=10){ //conditional
			System.out.println(i);
			i=i+1;//incremental/decremental part
		}
		
		for(int j=10;j>=0;j=j-1){ //(initialization;conditional;incremental)
			System.out.println(j);
		}
		
		
	}

}
